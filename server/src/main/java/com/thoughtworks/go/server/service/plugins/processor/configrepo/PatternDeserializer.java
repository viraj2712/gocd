package com.thoughtworks.go.server.service.plugins.processor.configrepo;

import com.google.gson.JsonDeserializationContext;
import com.google.gson.JsonDeserializer;
import com.google.gson.JsonElement;
import com.google.gson.JsonParseException;

import java.lang.reflect.Type;
import java.util.regex.Pattern;

public class PatternDeserializer implements JsonDeserializer<Pattern> {

    @Override
    public Pattern deserialize(JsonElement json, Type typeOfT, JsonDeserializationContext context) throws JsonParseException {
        String regex = json.getAsJsonPrimitive().getAsString();
        return Pattern.compile(regex);
    }
}
